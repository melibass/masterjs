//OPERADORES DE COMPARACION

//De igualdad
console.log( "hola" == "Hola");
console.log( "saludos" == "saludos");
console.log( 34 == 34);

//De igualdad estricta - se recomienda usar igualdad estricta para evitar errores.

console.log( 12 === "12"); //false
console.log( 12 === 12); //true

//Mayor que >
console.log(4 > 2);
console.log(4 > 32);
console.log(4 > 4); 

//Mayor o igual que >=
console.log(4 >= 2); // true
console.log(4 >= 4); // true

//Menor que <
console.log(4 < 2); // false
console.log(4 < 32);// true
console.log(4 < 4);  //false

//Menor o igual que <=
console.log(4 >= 2); // false
console.log(4 >= 4); // true

//Distinto que simple !=
console.log("hola" != "Hola"); // true

//Distinto que estricto !==
console.log( 45 !== 45); // false
console.log(45 !== "45"); // true


