//OPERADORES ARITMETICOS
let numeroA = 14;
let numeroB = 32;

//Suma +
let resultadoSuma = numeroA + numeroB + 10;
console.log(resultadoSuma);

//Resta -
let resultadoResta = numeroA - numeroB;
console.log(resultadoResta);

//Multiplicacion *
let resultadoMultiplicacion = numeroA * numeroB;
console.log(resultadoMultiplicacion);

//Division /
let resultadoDivision = numeroA / numeroB;
console.log(resultadoDivision);

//Modulo % -- es el resto de la division
let resultadoModulo = 10 % 2;
console.log(resultadoModulo);

//Operacion combinada
let operacionCombinada = (5 + 10) * 2;
console.log(operacionCombinada);
